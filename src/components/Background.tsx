export function Cloud({
  x = 0,
  y = 0,
  x_speed = 2,
  x_scale = 1,
  y_scale = 1,
  width = 400,
  height = 100,
  style,
  ...rest
}: Omit<ComponentProps<"svg">, "viewBox" | "xmlns"> & {
  x?: number; // starting X (%)
  y?: number; // Y position (px)
  x_speed?: number; // % per second
  x_scale?: number;
  y_scale?: number;
  width?: number;
  height?: number;
}) {
  const [xPos, setXPos] = useState(x);

  useInterval(() => {
    setXPos((prev) => {
      const next = prev + x_speed / 60; // 60fps equivalent
      // reset to -50% after leaving 150%
      if (next > 150) return -50;
      if (next < -50) return 150;
      return next;
    });
  }, 1000 / 60);

  return (
    <svg
      viewBox="0 0 400 100"
      width={width}
      height={height}
      style={{
        position: "absolute",
        left: `${xPos}%`,
        top: `${y}px`,
        transform: `scale(${x_scale}, ${y_scale})`,
        transformOrigin: "center",
        willChange: "transform, left",
        pointerEvents: "none",
        ...style,
      }}
      {...rest}
      role="img"
      aria-label="Cloud"
    >
      {[
        [200, 55, 180, 30],
        [60, 50, 55, 32],
        [340, 52, 58, 34],
        [130, 48, 48, 28],
        [270, 46, 52, 30],
        [200, 44, 50, 26],
        [100, 35, 42, 24],
        [180, 30, 45, 26],
        [240, 32, 40, 24],
        [310, 36, 38, 22],
        [150, 38, 35, 20],
        [280, 40, 38, 22],
        [220, 35, 32, 18],
      ].map(([cx, cy, rx, ry]) => (
        <ellipse
          key={JSON.stringify({ cx, cy, rx, ry })}
          cx={cx}
          cy={cy}
          rx={rx}
          ry={ry}
          fill="white"
        />
      ))}
    </svg>
  );
}
